    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        clave = txtClave.getText();
        repetir = txtRepetir.getText();
        if (clave.equalsIgnoreCase(repetir)){
            usuario.setClave(clave);
            new UsuarioDaoImp().upDateUsuario(usuario);
            JOptionPane.showMessageDialog(rootPane, "SU CLAVE A SIDO MODIFICADA EXITOSAMENTE", "CAMBIO DE CLAVE", 1);
        }else{
            JOptionPane.showMessageDialog(rootPane, "LAS CLAVES NO SON IGUALES, INTENTE NUEVAMENTE", "CAMBIO DE CLAVE", 1);
        }
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

