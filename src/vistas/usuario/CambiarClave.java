     private void permitirSoloNumero(java.awt.event.KeyEvent evt) {
          // permitir solo el ingreso de numero
         char caracter = evt.getKeyChar();
        if(((caracter < '0') || (caracter > '9')) && (caracter != '\b' /*corresponde a BACK_SPACE*/)){
            JOptionPane.showMessageDialog(rootPane, "DEBE INGRESAR SOLO NUMEROS", "ADVERTENCIA", 1);  // ignorar el evento de teclado
            txtClave.setText("");
            txtRepetir.setText("");
        }
       
        
    }    

